def compile_ea(ea_name="GannScalper"):
    """Compile an MQL5 EA. Returns (success: bool, log_text: str)."""
    mq5_file = f"{ea_name}.mq5"
    src = os.path.join(PROJECT, "mql5", mq5_file)
    dst = os.path.join(EA_DIR, mq5_file)
    log_path = os.path.join(PROJECT, "compile.log")

    # Copy source to EA directory
    if not os.path.exists(src):
        return False, f"Source file not found: {src}"

    with open(src, "rb") as f:
        data = f.read()
    with open(dst, "wb") as f:
        f.write(data)
    print(f"Copied {mq5_file} to {EA_DIR}")

    # Remove old log
    if os.path.exists(log_path):
        os.remove(log_path)

    # Compile using PowerShell Start-Process -Wait
    ps_cmd = (
        f'Start-Process -FilePath "{METAEDITOR}" '
        f'-ArgumentList \'/compile:"{dst}" /log:"{log_path}" /inc:"{MQL5_DATA}"\' '
        f'-Wait -NoNewWindow'
    )
    result = subprocess.run(
        ["powershell.exe", "-Command", ps_cmd],
        capture_output=True, text=True, timeout=120
    )

    # Read log
    if os.path.exists(log_path):
        with open(log_path, "rb") as f:
            raw = f.read()
        # Log is UTF-16
        try:
            log_text = raw.decode("utf-16")
        except:
            log_text = raw.decode("utf-8", errors="replace")

        # Check for errors
        match = re.search(r"Result:\s*(\d+)\s*error", log_text)
        errors = int(match.group(1)) if match else -1
        success = errors == 0

        # Filter to just important lines
        important = []
        for line in log_text.splitlines():
            line = line.strip()
            if not line:
                continue
            if "generating code" in line and "%" in line:
                continue  # Skip progress lines
            important.append(line)

        return success, "\n".join(important)
    else:
        return False, "Compilation log not found — MetaEditor may not have run"


