def run_test(ini_path=None, wait=True):
    """Launch MT5 Strategy Tester. Returns immediately if wait=False."""
    if ini_path is None:
        ini_path = os.path.join(PROJECT, "mt5_test.ini")

    if not os.path.exists(ini_path):
        print(f"Config not found: {ini_path}")
        return False

    print(f"Launching MT5 Strategy Tester...")
    print(f"Config: {ini_path}")

    if wait:
        ps_cmd = (
            f'Start-Process -FilePath "{TERMINAL}" '
            f'-ArgumentList \'/config:"{ini_path}"\' '
            f'-Wait'
        )
        result = subprocess.run(
            ["powershell.exe", "-Command", ps_cmd],
            capture_output=True, text=True, timeout=600
        )
        print("MT5 Strategy Tester finished.")
        return True
    else:
        subprocess.Popen(
            [TERMINAL, f"/config:{ini_path}"],
            creationflags=subprocess.CREATE_NEW_CONSOLE
        )
        print("MT5 launched in background. Use 'python mt5_tools.py results' to check.")
        return True


def read_results():
    """Read the most recent Strategy Tester results."""
    results = {}

    # 1. Find most recent tester log
    if os.path.exists(TESTER_LOGS):
        logs = sorted(glob.glob(os.path.join(TESTER_LOGS, "*.log")),
                       key=os.path.getmtime, reverse=True)
        if logs:
            log_file = logs[0]
            print(f"\n=== Tester Log: {os.path.basename(log_file)} ===")
            try:
                with open(log_file, "rb") as f:
                    raw = f.read()
                try:
                    text = raw.decode("utf-16")
                except:
                    text = raw.decode("utf-8", errors="replace")

                # Print last 100 lines (most relevant)
                lines = text.splitlines()
                for line in lines[-100:]:
                    print(line)
                results["tester_log"] = text
            except Exception as e:
                print(f"Error reading log: {e}")

    # 2. Find most recent report
    if os.path.exists(REPORTS_DIR):
        reports = sorted(glob.glob(os.path.join(REPORTS_DIR, "*.htm")),
                         key=os.path.getmtime, reverse=True)
        if reports:
            report_file = reports[0]
            print(f"\n=== Report: {os.path.basename(report_file)} ===")
            try:
                with open(report_file, "r", encoding="utf-8", errors="replace") as f:
                    html = f.read()

                # Extract key metrics from HTML
                metrics = extract_metrics_from_html(html)
                for k, v in metrics.items():
                    print(f"  {k}: {v}")
                results["report"] = metrics
                results["report_path"] = report_file
            except Exception as e:
                print(f"Error reading report: {e}")
        else:
            print(f"\nNo HTML reports found in {REPORTS_DIR}")
    else:
        print(f"\nReports directory not found: {REPORTS_DIR}")

    return results


def extract_metrics_from_html(html):
    """Extract key trading metrics from MT5 HTML report."""
    metrics = {}

    patterns = {
        "Total Trades": r'Total Trades.*?<td[^>]*>(\d+)',
        "Profit Trades": r'Profit Trades.*?<td[^>]*>([\d.]+%?)',
        "Loss Trades": r'Loss Trades.*?<td[^>]*>([\d.]+%?)',
        "Total Net Profit": r'Total Net Profit.*?<td[^>]*>([-\d\s.]+)',
        "Gross Profit": r'Gross Profit.*?<td[^>]*>([-\d\s.]+)',
        "Gross Loss": r'Gross Loss.*?<td[^>]*>([-\d\s.]+)',
        "Profit Factor": r'Profit Factor.*?<td[^>]*>([\d.]+)',
        "Expected Payoff": r'Expected Payoff.*?<td[^>]*>([-\d.]+)',
        "Sharpe Ratio": r'Sharpe Ratio.*?<td[^>]*>([-\d.]+)',
        "Balance Drawdown Maximal": r'Balance Drawdown Maximal.*?<td[^>]*>([-\d\s.%]+)',
        "Equity Drawdown Maximal": r'Equity Drawdown Maximal.*?<td[^>]*>([-\d\s.%]+)',
        "Recovery Factor": r'Recovery Factor.*?<td[^>]*>([-\d.]+)',
    }

    for name, pattern in patterns.items():
        match = re.search(pattern, html, re.DOTALL | re.IGNORECASE)
        if match:
            metrics[name] = match.group(1).strip()

    return metrics


